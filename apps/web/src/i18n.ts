export function getCurrentLocale(): I18nLocale {
  if (typeof window === "undefined") {
    return "zh-CN";
  }

  try {
    const raw = window.localStorage.getItem("x-file.preferences.profile");
    if (!raw) {
      return "zh-CN";
    }
    const language = (JSON.parse(raw) as { language?: unknown }).language;
    return language === "en-US" ? "en-US" : "zh-CN";
  } catch {
    return "zh-CN";
  }
}

export function getLocaleForIntl(): I18nLocale {
  return getCurrentLocale();
}

export function t(key: I18nKey | string, params?: TranslationParams): string {
  const normalizedKey = normalizeI18nKey(key);
  const locale = getCurrentLocale();
  const dictionary = dictionaries[locale];
  const template = dictionary[normalizedKey] ?? zhCN[normalizedKey] ?? key;
  if (!params || typeof template !== "string") {
    return String(template);
  }

  return Object.entries(params).reduce<string>((result, [name, value]) => {
    return result.replaceAll(
      `{${name}}`,
      value === null || value === undefined ? "" : String(value),
    );
  }, template);
}

function normalizeI18nKey(key: I18nKey | string): I18nKey {
  const rawKey = String(key);
  const plainKey = rawKey.startsWith("conversation.")
    ? rawKey.slice("conversation.".length)
    : rawKey;
  return (nestedKeyMap[plainKey] ?? plainKey) as I18nKey;
}
